#include<p16f877a.inc>
ORG 00H
BCF STATUS,C
BCF STATUS,DC
MOVLW B'00000001
MOVWF 0X21
MOVWF 0X22
MOVLW B'10000000
MOVWF 0X31
MOVWF 0X32
MOVLW B'00000111
MOVWF 0X23
MOVWF 0X33
MOVLW B'00000011
MOVWF 0X24
MOVWF 0X34
CALL SUBROUTINE
CALL SUBROUTINE1
GOTO END_1
NOP
SUBROUTINE:
	GOWTHAM 
		RLF 0X21,1
		DECFSZ 0X23,1
		GOTO GOWTHAM
		MOVLW B'00000001
		MOVWF 0X21
	GOWTHAM1
		RLF 0X21,1
		DECFSZ 0X24,1
		GOTO GOWTHAM1
		RETURN
SUBROUTINE1:  
	GOWTHAM2
		RRF 0X31,1
		DECFSZ 0X33,1
		GOTO GOWTHAM2
		MOVLW B'10000000
		MOVWF 0X31
	GOWTHAM3
		RRF 0X31,1
		DECFSZ 0X34,1
		GOTO GOWTHAM3
		RETURN
NOP
END_1
