					ON	
						MOVWF PORTB
						BTFSS PIR1 ,0
						GOTO ON
						GOTO SWITCH
					SWITCH
						MOVLW B'00000000
						MOVWF PIR1
						MOVLW B'11011100                             
						MOVWF TMR1L  ;TMR1 LSB BIT SETTING
						MOVLW B'10110000                                           
						MOVWF TMR1H ;TMR1 MSB BIT SETTING
						GOTO GOWTHAM
NOP
